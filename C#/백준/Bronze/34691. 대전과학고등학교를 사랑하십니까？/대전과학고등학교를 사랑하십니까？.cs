while (true)
{
    string str = Console.ReadLine();

    if (str == "end")
    {
        break;
    }
    if (str == "flower")
    {
        Console.WriteLine("Forsythia koreana");
    }
    if (str == "animal")
    {
        Console.WriteLine("Panthera tigris");
    }
    if (str == "tree")
    {
        Console.WriteLine("Pinus densiflora");
    }
}